    private void saveBillingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBillingActionPerformed
        boolean reqField = val.isNullOrEmpty(first_name.getText(), last_name.getText(), email.getText(), product.getSelectedItem().toString());
        if (reqField) {
            JOptionPane.showMessageDialog(null, "Field must not be empty");
        } else {
            try {
//                java.util.Date date=new java.util.Date();
//java.sql.Date purchase_date=new java.sql.Date(date.getTime());

                String[] expire;
                expire = duration.getText().split(" ");
                LocalDate today = LocalDate.now();
                // System.out.println(today.plusDays(Integer.parseInt(expire[0])-1));
                LocalDate expired_date=null;
                if (expire[1].equals("Year")) {
                    expired_date = today.plusYears((Integer.parseInt(expire[0])));
                }else{
                     expired_date = today.plusDays(Integer.parseInt(expire[0]));
                }
//java.sql.Timestamp sqlTime=new java.sql.Timestamp(date.getTime());
                String licence = GenerateLicence.createLicenseKey(email.getText(), product_code.getText(), expired_date + "");
                //System.out.println(licence);
                query = "insert into customer_tbl values(" + db.getNewId("id", "customer_tbl") + ",'" + first_name.getText() + "','" + last_name.getText() + "','" + email.getText() + "','" + product.getSelectedItem() + "','" + product_code.getText() + "','" + db_support.getText() + "'," + Integer.parseInt(max_client.getText()) + "," + Double.parseDouble(price.getText()) + ",'" + licence + "','" + today + "','" + expired_date + "','"+duration.getText()+"')";
                db.executeQueryData(query, "Data has been created");
                clearPurchaseAll();

            } catch (Exception ex) {
                Logger.getLogger(MainApp.class
                        .getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_saveBillingActionPerformed
