    public String[] getUUID() throws IOException {
            String uuid="";
        if (OS.indexOf("win") >= 0) {
            //for downgrade jvm
  uuid= Runtime.getRuntime().exec("wmic csproduct get UUID")+"";
            String[] arr= uuid.split("@");
            uuid=arr[1];            
//upgrade jvm
           // uuid= Runtime.getRuntime().exec("wmic csproduct get UUID").pid()+"";
        } else if (OS.indexOf("nix") >= 0 || OS.indexOf("nux") >= 0 || OS.indexOf("aix") > 0) {
          // uuid= Runtime.getRuntime().exec("cat /sys/class/dmi/id/product_uuid").pid()+"";
            uuid= Runtime.getRuntime().exec("cat /sys/class/dmi/id/product_uuid")+"";
            String[] arr= uuid.split("@");
            uuid=arr[1];
        }
      //  System.out.println(uuid);
        String[] res={OS,uuid};
        return res;
       
    }

